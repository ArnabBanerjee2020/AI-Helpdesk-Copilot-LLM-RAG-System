def rule_based_risk(ticket):

    ticket = ticket.lower()

    if any(
        word in ticket
        for word in [
            "outage",
            "breach",
            "attack",
            "ransomware"
        ]
    ):
        return "Critical"

    elif any(
        word in ticket
        for word in [
            "multiple users",
            "department",
            "vpn"
        ]
    ):
        return "High"

    elif any(
        word in ticket
        for word in [
            "access",
            "setup"
        ]
    ):
        return "Low"

    return "Medium"

